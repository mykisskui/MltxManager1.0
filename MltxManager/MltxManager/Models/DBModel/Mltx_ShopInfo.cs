using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MltxManager.Models.DBModel
{
    /// <summary>
    /// 门店管理表
    /// </summary>
    public class Mltx_ShopInfo
    {
        [Key]
        public int ShopId { get; set; }
        /// <summary>
        /// 门店名称
        /// </summary>
        public string ShopName { get; set; }
        /// <summary>
        /// 门店电话
        /// </summary>
        public string ShopTel { get; set; }
        /// <summary>
        /// 门店地址
        /// </summary>
        public string ShopAddr { get; set; }
        /// <summary>
        /// 纬度
        /// </summary>
        public string Shop_x { get; set; }
        /// <summary>
        /// 经度
        /// </summary>
        public string Shop_y { get; set; }
        /// <summary>
        /// 是否激活
        /// </summary>
        public Enable Enable { get; set; }
        public string Remark { get; set; }
    }

}
